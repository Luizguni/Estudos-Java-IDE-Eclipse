package br.comjava;

public class Varialvel {
	public static void main(String[]args) {
		String cesta = "Maça";
		System.out.println(cesta);
		
		cesta = "Laranja";
		System.out.println(cesta);
		cesta = "Uva";
		System.out.println(cesta);
		
		cesta = "Frutas";
		System.out.println(cesta);
 		
	}
}
